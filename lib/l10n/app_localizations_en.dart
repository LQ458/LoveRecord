  @override
  String formatRelativeTime(DateTime dateTime) {
    final now = DateTime.now();
    final difference = now.difference(dateTime);
    
    if (difference.inMinutes < 1) return justNow;
    if (difference.inHours < 1) return '${difference.inMinutes} $minutesAgo';
    if (difference.inDays < 1) return '${difference.inHours} $hoursAgo';
    if (difference.inDays < 7) return '${difference.inDays} $daysAgo';
    if (difference.inDays < 30) return '${(difference.inDays / 7).floor()} $weeksAgo';
    if (difference.inDays < 365) return '${(difference.inDays / 30).floor()} $monthsAgo';
    return '${(difference.inDays / 365).floor()} $yearsAgo';
  }
  
